def multiply(a,b):
	"""" Returns the multiplication of the inputs"""
	return a*b

def divide(a,b):
	"""" Returns the multiplication of the inputs"""
	if b==0:
		raise ZeroDivisionError
	return a/b

def modulus(a,b):
	"""" Returns the multiplication of the inputs"""
	return a%b

